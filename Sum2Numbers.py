# First we will get the first 2 numbers
number1 = float(input('Please input the first number: '))
number2 = float(input('Please input the second number number: '))

# Next next we will sum the 2 numbers
sum1 = number1 + number2

# Finally we will print the sum using an f print statement
print(f"The sum of {number1} and {number2} is {sum1}")